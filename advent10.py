class Pair:
    def __init__(self, open, close, score, acscore):
        self.open = open
        self.close = close
        self.score = score
        self.acscore = acscore


pairs = [
    Pair("(", ")", 3, 1),
    Pair("[", "]", 57, 2),
    Pair("{", "}", 1197, 3),
    Pair("<", ">", 25137, 4),
]

pairs_by_close = {p.close: p for p in pairs}
pairs_by_open = {p.open: p for p in pairs}

score = 0
autocomplete_scores = []
for line in input.split("\n"):
    if not line:
        continue
    stack = []
    has_error = False
    for s in line:
        if s in pairs_by_open.keys():
            stack.append(s)
        else:
            pair = pairs_by_close.get(s)
            last_open = stack.pop()
            if pair.open != last_open:
                score += pair.score
                has_error = True
                break

    if not has_error:
        acscore = 0
        while stack:
            s = stack.pop()
            acscore *= 5
            acscore += pairs_by_open.get(s).acscore
        autocomplete_scores.append(acscore)

print(score)  # <-- CORRECT
autocomplete_scores.sort()
middlescore = autocomplete_scores[len(autocomplete_scores) // 2]
